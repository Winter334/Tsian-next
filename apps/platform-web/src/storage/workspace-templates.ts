const ENTITY_READER_SCRIPT_JS = text([
  "function isRecord(value) { return typeof value === 'object' && value !== null && !Array.isArray(value); }",
  "function fail(code, message, details) { const error = new Error(message); error.code = code; if (details !== undefined) error.details = details; throw error; }",
  "function normalizeSegment(value, label) {",
  "  if (typeof value !== 'string') fail('ENTITY_ID_INVALID', label + ' must be a string.');",
  "  const segment = value.trim();",
  "  if (!segment || segment === '.' || segment === '..' || /[\\\\/:\\0]/.test(segment)) fail('ENTITY_ID_INVALID', label + ' must not be empty or contain path separators, colon, NUL, . or ...', { value });",
  "  return segment;",
  "}",
  "function pathFromId(id) {",
  "  if (typeof id !== 'string') fail('ENTITY_ID_REQUIRED', 'Entity id must be a string.');",
  "  const parts = id.split(':');",
  "  if (parts.length !== 2) fail('ENTITY_ID_INVALID', 'Entity id must use <type>:<localId>.', { id });",
  "  return 'save/entities/' + normalizeSegment(parts[0], 'Entity type') + '/' + normalizeSegment(parts[1], 'Entity localId') + '.json';",
  "}",
  "function normalizeEntityPath(value) {",
  "  if (typeof value !== 'string') fail('ENTITY_PATH_REQUIRED', 'Entity path must be a string.');",
  "  const path = value.trim().replace(/\\\\/g, '/').replace(/^\\/+/, '').replace(/\\/+/g, '/');",
  "  if (!/^save\\/entities\\/[^/]+\\/[^/]+\\.json$/.test(path)) fail('ENTITY_PATH_INVALID', 'Entity path must look like save/entities/<type>/<localId>.json.', { path: value });",
  "  if (path.split('/').some((segment) => !segment || segment === '.' || segment === '..')) fail('ENTITY_PATH_INVALID', 'Entity path must not contain empty, current, or parent directory segments.', { path: value });",
  "  return path;",
  "}",
  "async function readEntity(input, tsian, signal) {",
  "  try {",
  "    signal.throwIfAborted();",
  "    if (!isRecord(input)) fail('ENTITY_INPUT_INVALID', 'read_entity input must be an object.');",
  "    const path = typeof input.id === 'string' && input.id.trim() ? pathFromId(input.id) : normalizeEntityPath(input.path);",
  "    tsian.trace('entity_read_started', { path, id: input.id });",
  "    const file = await tsian.workspace.read({ scope: 'effective', path });",
  "    let data;",
  "    try { data = JSON.parse(file.content); } catch (error) { fail('ENTITY_JSON_INVALID', 'Entity file is not valid JSON: ' + (error && error.message || error), { path }); }",
  "    if (!isRecord(data)) fail('ENTITY_JSON_OBJECT_REQUIRED', 'Entity JSON must be an object.', { path });",
  "    tsian.trace('entity_read_completed', { path, id: data.id });",
  "    return { path, id: typeof data.id === 'string' ? data.id : input.id, data };",
  "  } catch (error) {",
  "    tsian.trace('entity_read_failed', { code: error && error.code || 'ENTITY_READ_FAILED', message: error && error.message || String(error) });",
  "    throw error;",
  "  }",
  "}",
  "return readEntity(input, tsian, signal);",
])

