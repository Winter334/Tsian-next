const MAINTENANCE_SCRIPT_COMMON = text([
  "function isRecord(value) {",
  "  return typeof value === 'object' && value !== null && !Array.isArray(value);",
  "}",
  "",
  "function fail(code, message, details) {",
  "  const error = new Error(message);",
  "  error.code = code;",
  "  if (details !== undefined) error.details = details;",
  "  throw error;",
  "}",
  "",
  "function normalizePath(value, codePrefix) {",
  "  if (typeof value !== 'string') fail(codePrefix + '_PATH_REQUIRED', 'Write path must be a string.');",
  "  const raw = value.trim();",
  "  const hadTrailingSlash = /[\\\\/]$/.test(raw);",
  "  const normalized = raw.replace(/\\\\/g, '/').replace(/^\\/+/, '').replace(/\\/+/g, '/').replace(/\\/+$/, '');",
  "  if (!normalized) fail(codePrefix + '_PATH_REQUIRED', 'Write path is required.');",
  "  if (hadTrailingSlash) fail(codePrefix + '_FILE_PATH_REQUIRED', 'Write path must not end with a slash.', { path: value });",
  "  const segments = normalized.split('/');",
  "  if (segments.some((segment) => segment === '.' || segment === '..' || segment === '')) {",
  "    fail(codePrefix + '_PATH_INVALID', 'Write path must not contain empty, current, or parent directory segments.', { path: value });",
  "  }",
  "  if (normalized === '.tsian' || normalized.startsWith('.tsian/')) {",
  "    fail(codePrefix + '_PLATFORM_METADATA_FORBIDDEN', 'Writes cannot target .tsian platform metadata.', { path: normalized });",
  "  }",
  "  return normalized;",
  "}",
  "",
  "function inferMediaType(path) {",
  "  if (path.endsWith('.json')) return 'application/json';",
  "  if (path.endsWith('.md')) return 'text/markdown';",
  "  return 'text/plain';",
  "}",
])

