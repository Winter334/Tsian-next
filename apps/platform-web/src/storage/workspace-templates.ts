  "const PLAN_SCHEMA = 'tsian.runtime.maintenance.plan.v1';",
  "const MAX_WRITES = 12;",
  "const MAX_CONTENT_LENGTH = 200000;",
  "",
  "function isAllowedTarget(path) {",
